// Header guard to prevent multiple inclusion of the same header file.
#ifndef PIXEL_H
#define PIXEL_H

// Definition of a simple structure representing the color information of a pixel.
struct Pixel {
    int r;  // Red component of the pixel color.
    int g;  // Green component of the pixel color.
    int b;  // Blue component of the pixel color.
};

#endif // PIXEL_H
